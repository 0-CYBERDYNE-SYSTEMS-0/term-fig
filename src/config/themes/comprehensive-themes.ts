// Helper to filter duplicates
export function filterDuplicateThemes(
  themes: Record<string, { name: string; colors: any }>
): Record<string, { name: string; colors: any }> {
  const seen = new Set<string>();
  const result: Record<string, { name: string; colors: any }> = {};
  
  for (const [key, theme] of Object.entries(themes)) {
    const colorHash = JSON.stringify(theme.colors);
    if (!seen.has(colorHash)) {
      seen.add(colorHash);
      result[key] = theme;
    }
  }
  
  return result;
}
